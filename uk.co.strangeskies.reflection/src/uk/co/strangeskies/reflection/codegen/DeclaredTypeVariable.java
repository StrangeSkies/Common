package uk.co.strangeskies.reflection.codegen;

import java.lang.reflect.GenericDeclaration;
import java.lang.reflect.Type;
import java.lang.reflect.TypeVariable;

/**
 * This type is a placeholder for a {@link TypeVariable} on a
 * {@link GenericDeclaration} produced from a {@link ParameterizedDeclaration}.
 * 
 * @author Elias N Vasylenko
 */
public class DeclaredTypeVariable implements Type {
	private final String name;

	public static DeclaredTypeVariable declaredTypeVariable(String ofName) {
		return new DeclaredTypeVariable(ofName);
	}

	/**
	 * @param name
	 *          the name of the declared type parameter
	 */
	protected DeclaredTypeVariable(String name) {
		this.name = name;
	}

	@Override
	public String getTypeName() {
		return name;
	}

	@Override
	public int hashCode() {
		return name.hashCode();
	}

	@Override
	public boolean equals(Object obj) {
		if (!(obj instanceof DeclaredTypeVariable))
			return false;
		return super.equals(obj);
	}
}
