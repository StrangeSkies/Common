package uk.co.strangeskies.observable;

import static java.util.function.Function.identity;

import org.junit.Test;

import mockit.Injectable;

@SuppressWarnings("javadoc")
public class FlatMappingObserverTest {
  @Injectable
  Observation upstreamObservation;

  @Injectable
  RequestAllocator requestAllocator;

  @Injectable
  Observer<String> downstreamObserver;

  @Test(expected = NullPointerException.class)
  public void nullMappingTest() {
    new FlatMappingObserver<>(downstreamObserver, null, requestAllocator);
  }

  @Test(expected = NullPointerException.class)
  public void nullRequestAllocatorTest() {
    new FlatMappingObserver<>(downstreamObserver, identity(), null);
  }
}
